function getLang() {
  return localStorage.getItem('lang') || 'th';
}

function setLang(lang) {
  localStorage.setItem('lang', lang);
  applyLang(lang);
  updateLangBtn(lang);
  // Fire custom event so pages can re-render dynamic content
  document.dispatchEvent(new CustomEvent('langchange', { detail: lang }));
}

function applyLang(lang) {
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else if (el.tagName === 'OPTION') {
        el.textContent = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  // Handle separate placeholder keys
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });
  document.documentElement.lang = lang;
}

// Helper to get a translation value directly
window.i18nGet = function(key) {
  const lang = getLang();
  return (translations[lang] && translations[lang][key]) || key;
};

function updateLangBtn(lang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function initI18n() {
  const lang = getLang();
  const run = () => {
    applyLang(lang);
    updateLangBtn(lang);
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });
  };
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', run);
  } else {
    run();
  }
}

window.initI18n = initI18n;

// Auto-init when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
